import { useEffect, useState } from "react";
import './App.css';
import logo from './mlh-prep.png'
import WeatherMap from "./components/WeatherMap"

function App() {
  const [error, setError] = useState(null);
  const [isLoaded, setIsLoaded] = useState(false);
  const [city, setCity] = useState("New York City")
  const [results, setResults] = useState(null);
  const [currLatitude, setcurrLatitude] = useState(51.505);
  const [currLongitude, setcurrLongitude] = useState(-0.09);

  useEffect(() => {
    fetch(`https://api.openweathermap.org/data/2.5/weather?q=${city}&units=metric&appid=${process.env.REACT_APP_APIKEY}`)
      .then(res => res.json())
      .then(
        (result) => {
          if (result['cod'] !== 200) {
            setIsLoaded(false)
          } else {
            console.log
            console.log(result.coord);
            setcurrLatitude(result.coord.lat);
            setcurrLongitude(result.coord.lon);
            setIsLoaded(true);
            setResults(result);
          }
        },
        (error) => {
          setIsLoaded(true);
          setError(error);
        }
      )
  }, [city])

  if (error) {
    return <div>Error: {error.message}</div>;
  } else {
    return <>
      <img className="logo" src={logo} alt="MLH Prep Logo"></img>
      <div>
        <h2>Enter a city below ðŸ‘‡</h2>
        <input
          type="text"
          value={city}
          onChange={event => setCity(event.target.value)} />
        <div className="Results">
          {!isLoaded && <h2>Loading...</h2>}
          {console.log(results)}
          {isLoaded && results && <>
            <h3>{results.weather[0].main}</h3>
            <p>Feels like {results.main.feels_like}Â°C</p>
            <i><p>{results.name}, {results.sys.country}</p></i>
          </>}
        </div>
       
      </div>
      <div className="Weather-map"  >  <WeatherMap cLatitude = {currLatitude} cLongitude = {currLongitude} /></div>
    </>
 
  }




}

export default App;
